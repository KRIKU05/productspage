document.addEventListener('DOMContentLoaded', () => {
    const latestBhajanAudio = document.getElementById('latestBhajanAudio');
    const latestBhajanBtn = document.querySelector('.latestBhajanBtn');

    const defaultWelcomeAudio = document.getElementById('defaultWelcomeAudio');
    const defaultAudioPlayBtn = document.getElementById('defaultAudioPlayBtn');

    if (latestBhajanBtn && latestBhajanAudio) {
        latestBhajanBtn.addEventListener('click', () => {
            const icon = latestBhajanBtn.querySelector('i');

            if (defaultWelcomeAudio && !defaultWelcomeAudio.paused) {
                defaultWelcomeAudio.pause();
            }

            if (latestBhajanAudio.paused) {
                latestBhajanAudio.play();
                icon.classList.replace('fa-play', 'fa-pause');
            } else {
                latestBhajanAudio.pause();
                icon.classList.replace('fa-pause', 'fa-play');
            }
        });

        latestBhajanAudio.addEventListener('ended', () => {
            const icon = latestBhajanBtn.querySelector('i');
            if (icon) icon.classList.replace('fa-pause', 'fa-play');
        });
    }

    if (defaultAudioPlayBtn && defaultWelcomeAudio) {
        defaultAudioPlayBtn.addEventListener('click', () => {
            if (latestBhajanAudio && !latestBhajanAudio.paused) {
                latestBhajanAudio.pause();
                const icon = latestBhajanBtn.querySelector('i');
                if (icon) icon.classList.replace('fa-pause', 'fa-play');
            }
            if (defaultWelcomeAudio.paused) {
                defaultWelcomeAudio.play();
            } else {
                defaultWelcomeAudio.pause();
            }
        });
    }
});
